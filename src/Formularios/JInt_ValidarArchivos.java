    private void jCButtonValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCButtonValidarActionPerformed
        // TODO add your handling code here:
        if (jTableListadoArchivos.getSelectedRow() >= 0) {
            String estadoAr = modeloA.getValueAt(jTableListadoArchivos.getSelectedRow(), 7).toString();
            idarc = modeloA.getValueAt(jTableListadoArchivos.getSelectedRow(), 0).toString();
            if (estadoAr.equals("0")) {
                validarArchivo();
                cargarTabla();
                cargarTablaArc(numacta);
            } else {
                mensaje.mensajeInformacion("El archivo seleccionado ya está validado.");
            }
        } else {
            mensaje.mensajeInformacion("Debe seleccionar un archivo para validar.");
        }
    }//GEN-LAST:event_jCButtonValidarActionPerformed

