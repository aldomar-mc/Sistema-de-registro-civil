    private void jTableListadoRegistrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableListadoRegistrosMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            if (jTableListadoRegistros.getSelectedRowCount() == 1) {
                numacta = modelo.getValueAt(jTableListadoRegistros.getSelectedRow(), 7).toString();
                jLabelApellidosNombres.setText(modelo.getValueAt(jTableListadoRegistros.getSelectedRow(), 2).toString());
                jLabelTipoRegistro.setText(modelo.getValueAt(jTableListadoRegistros.getSelectedRow(), 6).toString());
                jLabelNumActa.setText(modelo.getValueAt(jTableListadoRegistros.getSelectedRow(), 7).toString());
                cargarTablaArc(numacta);
            }
        }
    }//GEN-LAST:event_jTableListadoRegistrosMouseClicked

