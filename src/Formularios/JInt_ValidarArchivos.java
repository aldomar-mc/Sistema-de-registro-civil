    public void tituloTabla() {
        String[] titulo = {"ID", "DNI", "APELLIDOS Y NOMBRES", "GENERO", "DIRECCIÓN", "FEC. NAC.", "TIPO REGISTRO",
            "N° ACTA", "DISTRITO", "PROVINCIA", "DEPARTAMENTO", "FEC. REG.", "HRA. REG."};
        modelo = new DefaultTableModel(null, titulo);
        modelo.setColumnIdentifiers(titulo);
        jTableListadoRegistros.setModel(modelo);
    }

    public void cargarTabla() {
        tabla.MostrarEnTabla("CALL listRegistrosVal()", jTableListadoRegistros, modelo, 13);
        tabla.ocultarColumnasTabla(jTableListadoRegistros, 0);
        tabla.setMaxWidthColumnTable(jTableListadoRegistros, 70, 1);
        tabla.setMaxWidthColumnTable(jTableListadoRegistros, 250, 2);
    }

    public void buscarArchivo() {
        controlador.BuscarDato("CALL searchRegistrosVal(?)", modelo, 13, jCTextFieldBuscar);
    }

    public void cargarTablaFiltro(String dato) {
        tabla.MostrarEnTabla("CALL listRegistrosValFiltrar('" + dato + "')", jTableListadoRegistros, modelo, 13);
        tabla.ocultarColumnasTabla(jTableListadoRegistros, 0);
        tabla.setMaxWidthColumnTable(jTableListadoRegistros, 70, 1);
        tabla.setMaxWidthColumnTable(jTableListadoRegistros, 250, 2);
    }

    public void tituloTablaArc() {
        String[] titulo = {"ID", "DOCUMENTO", "N° LIBRO", "ARCHIVO", "FEC. SUBIDO", "HRA. SUBIDO", "RUTA ARCHIVO", "ESTADO"};
        modeloA = new DefaultTableModel(null, titulo);
        modeloA.setColumnIdentifiers(titulo);
        jTableListadoArchivos.setModel(modeloA);
        //Ocultamos las columnas
        tabla.ocultarColumnasTabla(jTableListadoArchivos, 0);
        tabla.ocultarColumnasTabla(jTableListadoArchivos, 6);
        tabla.setMaxWidthColumnTable(jTableListadoArchivos, 400, 3);
    }

    public void cargarTablaArc(String numac) {
        tabla.MostrarEnTabla("CALL listArchivosVal('" + numac + "')", jTableListadoArchivos, modeloA, 8);
        //Ocultamos las columnas
        tabla.ocultarColumnasTabla(jTableListadoArchivos, 0);
        tabla.ocultarColumnasTabla(jTableListadoArchivos, 6);
        tabla.setMaxWidthColumnTable(jTableListadoArchivos, 400, 3);
    }

    public void validarArchivo() {
        try {
            String sql = "CALL editValidarArchivo("
                    + "'" + idarc + "'"
                    + " );";
            mensaje.mensajeInformacion(consulta.CrearRegistro(sql));
        } catch (Exception e) {
            mensaje.mensajeError("Validar el archivo");
        }
    }
    public void desvalidarArchivo() {
        try {
            String sql = "CALL editDesvalidarArchivo("
                    + "'" + idarc + "'"
                    + " );";
            mensaje.mensajeInformacion(consulta.CrearRegistro(sql));
        } catch (Exception e) {
            mensaje.mensajeError("Desvalidar el archivo");
        }
    }

