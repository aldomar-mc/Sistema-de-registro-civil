    private void jCButtonAgregarRNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCButtonAgregarRNActionPerformed
        // TODO add your handling code here:
        if (validarCampos()) {
            if (jCButtonAgregarRN.getText().equals("Aceptar")) {
                try {
                    String sql = "CALL updateFecMatrimonio("
                            + "'" + fecha.Formato_AMD(jDateChooserFecha.getDate()) + "',"
                            + "'" + fecha.HoraMysql(timeChooserHora.getText()) + "',"
                            + "'" + JInt_Registro.idregistroFecRM + "'"
                            + " );";
                    mensaje.mensajeInformacion(consulta.CrearRegistro(sql));
                    mensaje.cerrarVentanaSinConfirmarJI(this);
                } catch (Exception e) {
                    mensaje.mensajeError("Error al actualizar la fecha del matrimonio");
                }
            } else if (jCButtonAgregarRN.getText().equals("Modificar")) {
                try {
                    String sql = "CALL updateFecMatrimonio("
                            + "'" + fecha.Formato_AMD(jDateChooserFecha.getDate()) + "',"
                            + "'" + fecha.HoraMysql(timeChooserHora.getText()) + "',"
                            + "'" + JInt_Registro.idregistroFecRD + "'"
                            + " );";
                    mensaje.mensajeInformacion(consulta.CrearRegistro(sql));
                    mensaje.cerrarVentanaSinConfirmarJI(this);
                } catch (Exception e) {
                    mensaje.mensajeError("Error al actualizar la fecha de defunción");
                }
            } else {
                if (jLabelFecha.getText().equals("Fecha de matrimonio*")) {
                    horaImportante = fecha.HoraMysql(timeChooserHora.getText());
                    fechaImportante = fecha.Formato_AMD(jDateChooserFecha.getDate());
                    mensaje.mensajeInformacion("Agregado correctamente");
                    mensaje.cerrarVentanaSinConfirmarJI(this);
                } else {
                    horaImportante = fecha.HoraMysql(timeChooserHora.getText());
                    fechaImportante = fecha.Formato_AMD(jDateChooserFecha.getDate());
                    mensaje.mensajeInformacion("Agregado correctamente");
                    mensaje.cerrarVentanaSinConfirmarJI(this);
                }
            }

        }
    }//GEN-LAST:event_jCButtonAgregarRNActionPerformed

