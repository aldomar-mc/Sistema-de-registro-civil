    public boolean validarCampos() {
        boolean valor = false;
        if (jDateChooserFecha.getDate() != null) {
            if (timeChooserHora.getText().length() != 0) {
                valor = true;
            } else {
                mensaje.mensajeError("Debe ingresar la " + jLabelHora.getText() + ".");
                timeChooserHora.requestFocus();
            }
        } else {
            mensaje.mensajeError("Debe ingresar la " + jLabelFecha.getText() + ".");
            jDateChooserFecha.requestFocusInWindow();
        }
        return valor;
    }

