    public void tituloTabla() {
        String[] titulo = {"DNI", "APELLIDOS Y NOMBRES", "NÂ° ACTA", "TIPO DOC.", "ARCHIVO", "HASH", "FEC. SUBIDO", "HRA. SUBIDO", "USUARIO"};
        modelo = new DefaultTableModel(null, titulo);
        modelo.setColumnIdentifiers(titulo);
        jTableListadoArchivosHash.setModel(modelo);
    }

    public void columnas() {
        tabla.setMaxWidthColumnTable(jTableListadoArchivosHash, 70, 0);
        tabla.setMaxWidthColumnTable(jTableListadoArchivosHash, 250, 1);
        tabla.setMaxWidthColumnTable(jTableListadoArchivosHash, 260, 4);
        tabla.setMaxWidthColumnTable(jTableListadoArchivosHash, 300, 5);
    }

    public void cargarTabla() {
        tabla.MostrarEnTabla("CALL listArchivosHash()", jTableListadoArchivosHash, modelo, 9);
        columnas();
    }

    public void buscarArchivo() {
        controlador.BuscarDato("CALL searchArchivosHash(?)", modelo, 9, jCTextFieldBuscaR);
    }

    public void cargarTablaFiltro(String dato) {
        tabla.MostrarEnTabla("CALL listArchivosHashFiltrar('" + dato + "')", jTableListadoArchivosHash, modelo, 9);
        columnas();
    }

